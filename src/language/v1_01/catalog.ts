export function getDslEntriesByGroup(groupId: DslCatalogGroupId): DslCatalogEntry[] {
  return dslCatalogEntries.filter((entry) => entry.group === groupId);
}

export function getDslAlphabeticalIndex(): DslCatalogEntry[] {
  return [...dslCatalogEntries].sort((left, right) => entrySortKey(left).localeCompare(entrySortKey(right)));
}

export function getSupportedDslEntries(): DslCatalogEntry[] {
  return dslCatalogEntries.filter((entry) => entry.status === "supported");
}

export function getPlannedDslEntries(): DslCatalogEntry[] {
  return dslCatalogEntries.filter((entry) => entry.status === "planned");
}

export function entrySortKey(entry: DslCatalogEntry): string {
  return `${entry.name} ${entry.symbol}`.toLowerCase();
}

